public class Stats {
	int strength;
	int dexterity;
	int constitution;
	int intelligence;
	int wisdom;
	int charisma;
	int hitpoints;
	int armor;
	int speed;
	String name;
	
	//Getters
	public int getStrength() {
		return strength;
	}
	public int getDexterity() {
		return dexterity;
	}
	public int getConstitution() {
		return constitution;
	}
	public int getIntelligence() {
		return intelligence;
	}
	public int getWisdom() {
		return wisdom;
	}
	public int getCharisma() {
		return charisma;
	}
	public int getHitpoints() {
		return hitpoints;
	}
	public int getArmorClass() {
		return armor;
	}
	public int getSpeed() {
		return speed;
	}
	public String getName() {
		return name;
	}
	//Setters
	public void setStrength(int newStrength) {
		this.strength = newStrength;
	}
	public void setDexterirty(int newDexterity) {
		this.dexterity = newDexterity;
	}
	public void setConstitution(int newConstitution) {
		this.constitution = newConstitution;
	}
	public void setIntelligence(int newIntelligence) {
		this.intelligence = newIntelligence;
	}
	public void setWisdom(int newWisdom) {
		this.wisdom = newWisdom;
	}
	public void setCharisma(int newCharisma) {
		this.charisma = newCharisma;
	}
	public void setHitpoints(int newHitpoints) {
		this.hitpoints = newHitpoints;
	}
	public void setArmorClass(int newArmor) {
		this.armor = newArmor;
	}
	public void setSpeed(int newSpeed) {
		this.speed = newSpeed;
	}
	public void setName(String newName) {
		this.name = newName;
	}
}
